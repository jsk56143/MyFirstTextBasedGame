public class Lamp {

    private boolean hasLamp, isLit;

    public Lamp() {
        hasLamp = false;
        isLit = false;
    }

   public void setHasLamp(boolean hasLamp) {
        this.hasLamp = hasLamp;
   }

   public void setIsLit(boolean isLit) {
        this.isLit = isLit;
   }

   public boolean getHasLamp() {
        return hasLamp;
   }

}
